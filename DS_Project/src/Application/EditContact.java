    private void Search_ECActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Search_ECActionPerformed
        // TODO add your handling code here:
        HT = p.get_HT();
        String n = Search_Contact_EC.getText();
        JFrame f;
        try {
            Contact c = HT.search(n);
            Name_EC.setText(c.name);
            PrimaryNum_EC.setText(c.Num1);
            SecondaryNum_EC.setText(c.Num2);
        } catch (NullPointerException e) {
            f = new JFrame();  
            JOptionPane.showMessageDialog(f, "Contact Does Not Exist.");
        }
    }//GEN-LAST:event_Search_ECActionPerformed

