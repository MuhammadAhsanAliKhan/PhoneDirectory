    private void Update_ECActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update_ECActionPerformed
        // TODO add your handling code here:
        HT = p.get_HT();
        HT.Delete(Search_Contact_EC.getText());
        JFrame f;
        Contact Updated;
        if (PrimaryNum_EC.getText().isBlank() && !SecondaryNum_EC.getText().isBlank()){
            PrimaryNum_EC.setText(SecondaryNum_EC.getText());
            if(Name_EC.getText().isBlank()) Updated = new Contact("Contact: " + PrimaryNum_EC.getText(),SecondaryNum_EC.getText(),"" );
            else Updated = new Contact(Name_EC.getText(),SecondaryNum_EC.getText(),"" );
            HT.insert(Updated);
            f = new JFrame();
            JOptionPane.showMessageDialog(f, "Contact Updated Sucessfully.");
        this.hide();
        }
        else if(PrimaryNum_EC.getText().isBlank() && SecondaryNum_EC.getText().isBlank()){
            f = new JFrame();
            JOptionPane.showMessageDialog(f, "As there are no Contact Numbers, \n Contact is Deleted");
            this.hide();
        }
        else{
            if(Name_EC.getText().isBlank()) 
                Updated = new Contact("Contact: " + PrimaryNum_EC.getText(), PrimaryNum_EC.getText(), SecondaryNum_EC.getText());
            else      
                Updated = new Contact(Name_EC.getText(), PrimaryNum_EC.getText(), SecondaryNum_EC.getText());
                HT.insert(Updated);
                f = new JFrame();  
                    JOptionPane.showMessageDialog(f, "Contact Updated Sucessfully.");
                this.hide();
        }
        
        
    }//GEN-LAST:event_Update_ECActionPerformed

